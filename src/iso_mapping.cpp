QStringList get_languages(const QHash<QString, QString>& lang_map) {
    QStringList languages;
    for (QHash<QString, QString>::const_iterator it = lang_map.constBegin(); it != lang_map.constEnd(); ++it)
        languages << it.value();
    return languages;
}

